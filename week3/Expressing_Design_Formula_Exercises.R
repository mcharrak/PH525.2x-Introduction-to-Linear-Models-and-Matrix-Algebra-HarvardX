## week 3 exercise: Expressing Design Formula Exercises

## exercise 1

# we wish to test the effect of condition on the outcome. so condition is IV and outcome y is DV.
# we wish to control for whatever differences that might have occured (i.e. confounders)
# such as the different days on which the measurements were taken.
# there is no interaction between condition and day.

# ANSWER: y ~ day + condition
# ==> ~ day + condition